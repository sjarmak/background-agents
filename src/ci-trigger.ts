/**
 * CI/GitHub Actions integration.
 *
 * Handles:
 * - Posting violation results as PR comments
 * - Reading GitHub Actions event context
 * - Setting exit codes based on violation severity
 */

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface CIContext {
  /** "pr" | "schedule" | "manual" */
  triggerType: "pr" | "schedule" | "manual";
  /** GitHub repository (owner/repo) */
  repo?: string;
  /** PR number (if PR-triggered) */
  prNumber?: number;
  /** Git ref (branch or SHA) */
  ref?: string;
  /** GitHub token for API calls */
  githubToken?: string;
}

export interface PRCommentPayload {
  owner: string;
  repo: string;
  prNumber: number;
  body: string;
  token: string;
}

// ---------------------------------------------------------------------------
// Context detection
// ---------------------------------------------------------------------------

/**
 * Detect CI context from environment variables (GitHub Actions).
 */
export function detectCIContext(): CIContext {
  const eventName = process.env.GITHUB_EVENT_NAME;
  const repo = process.env.GITHUB_REPOSITORY;
  const ref = process.env.GITHUB_REF;
  const token = process.env.GITHUB_TOKEN;

  // PR trigger
  if (eventName === "pull_request" || eventName === "pull_request_target") {
    const prNumber = ref ? parseInt(ref.split("/")[2], 10) : undefined;
    return {
      triggerType: "pr",
      repo,
      prNumber: Number.isNaN(prNumber) ? undefined : prNumber,
      ref,
      githubToken: token,
    };
  }

  // Schedule trigger
  if (eventName === "schedule") {
    return { triggerType: "schedule", repo, ref, githubToken: token };
  }

  // Manual or unknown
  return { triggerType: "manual", repo, ref, githubToken: token };
}

